    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtIdProducto.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar un Producto");
            btnBuscarProducto.requestFocus();
            return;
        }
        if(txtCantidad.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una Cantidad");
            txtCantidad.requestFocus();
            return;
        }
        if(txtPrecioventaC.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Precio");
            txtPrecioventaC.requestFocus();
            return;
        }
        if(cboEstadoR.getSelectedIndex() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Estado del Consumo");
            cboEstadoR.requestFocus();
            return;
        }

        Consumo dts =new Consumo();
        CConsumo produ =new CConsumo();
        dts.setIdReserva(Integer.parseInt(txtIdReserva.getText()));
        dts.setIdProducto(Integer.parseInt(txtIdProducto.getText()));
        dts.setCantidad(Double.parseDouble(txtCantidad.getText()));
        dts.setPrecioVenta(Double.parseDouble(txtPrecioventaC.getText()));
        int seleccionado=cboEstadoR.getSelectedIndex();
        dts.setEstado((String)cboEstadoR.getItemAt(seleccionado));
        if(accion.equals("guardar")){
            if(produ.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El Consumo "+txtNombreProducto.getText()+" del Cliente "+txtNombreCliente.getText()+" fue Registrado");
                mostrar(idreserva);
                inhabilitar();
            }

        }
        else if (accion.equals("editar")){
            dts.setIdConsumo(Integer.parseInt(txtidC.getText()));
            if(produ.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El Consumo del Cliente "+txtNombreCliente.getText()+" fue Actualizado");
                mostrar(idreserva);
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

