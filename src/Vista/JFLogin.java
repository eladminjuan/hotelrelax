    private void txtUsuarioLKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioLKeyReleased
       Boolean vl=ValidarEmail(txtUsuarioL.getText());
       if(!vl==false){
           che.setText("");
           ingrasr.setEnabled(true);
           txtPasswordL.setEnabled(true);
           nota.setText("");
       }else if(txtUsuarioL.getText().length() == 0){
           che.setText("");
           ingrasr.setEnabled(false);
           txtPasswordL.setEnabled(false);
           nota.setText("");
       }else{
           che.setText("*");
           ingrasr.setEnabled(false);
           txtPasswordL.setEnabled(false);
           nota.setText("Los Campos Con (*) son declarados No VÃ¡lido");
       }
    }//GEN-LAST:event_txtUsuarioLKeyReleased

