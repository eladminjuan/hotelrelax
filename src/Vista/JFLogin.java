    private void ingrasrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingrasrActionPerformed
       
        try {
            DefaultTableModel modelo;
            CEmpleado func = new CEmpleado();
            Empleado dts = new Empleado();
            
            

            dts.setLogin(txtUsuarioL.getText());
            dts.setPassword(txtPasswordL.getText());

            modelo = func.login(dts.getLogin(), dts.getPassword());

            tablaListado.setModel(modelo);
            if (func.totalregistros > 0) {
                this.dispose();
                JFMenu form = new JFMenu();
                form.toFront();
                form.setVisible(true);
                JFMenu.lblidpersona.setText(tablaListado.getValueAt(0, 0).toString());
                JFMenu.lblNombre.setText(tablaListado.getValueAt(0, 1).toString());
                JFMenu.lblAPaterno.setText(tablaListado.getValueAt(0, 2).toString());
                JFMenu.lblAMaterno.setText(tablaListado.getValueAt(0, 3).toString());
                JFMenu.lblAcceso.setText(tablaListado.getValueAt(0, 4).toString());
                JFMenu.lblPresentacion.setText(JFMenu.lblNombre.getText() + " " + JFMenu.lblAPaterno.getText() + " " + JFMenu.lblAMaterno.getText());
                Icon perfil;
                perfil = new ImageIcon(getClass().getResource("/Files/emblem.png"));
                JFMenu.lblFoto.setIcon(perfil);
                if (!JFMenu.lblAcceso.getText().equals("Administrador")) {
                    JFMenu.mnuArchivo.setEnabled(false);
                    JFMenu.mnuConfiguraciones.setEnabled(false);
                    perfil = new ImageIcon(getClass().getResource("/Files/fotoR.png"));
                    JFMenu.lblFoto.setIcon(perfil);
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Acceso Denegado", "Acceso al Sistema", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_ingrasrActionPerformed

